#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb == 0)
	{
		return (0);
	}
	if (nb == 1)
	{
		return (1);
	}
	while ((nb / 2) > i)
	{
		if (nb == i * i)
		{
			return (i);
		}
		else if (i >= 46341)
		{
			return (0);
		}
		i++;
	}
	return (0);
}
