#include <unistd.h>
#include <stdio.h>

int	main(int ac, char **str)
{
	int	i;

	//ac = 2;
	i = 0;
	if ((str[1][i] != ac && str[1][i] == '\0') || str[1][i] == '\t')
	{
		write (1, "\n", 1);
	}
	while (str[1][i] != '\0')
	{
		if (str[1][i] != '\t' && str[1][i] != ' ')
		{
			write (1, &str[1][i], 1);
			i++;
		}
	}
	return (0);
}
